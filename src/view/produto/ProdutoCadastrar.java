    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
       if (jTextFieldNome.getText().trim().equals("") || jTextFieldUnidadeMedida.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos");
            jTextFieldNome.requestFocus();
            return;
        }
        
        String nome = jTextFieldNome.getText();
        String unidadeMedida = jTextFieldUnidadeMedida.getText();
        
        Produto p = new Produto();
        p.setNome(nome);
        p.setUnidadeDeMedida(unidadeMedida);

        //Inserir no BD
        try {
            ProdutoDao dao = new ProdutoDao();
            dao.inserir(p);
            JOptionPane.showMessageDialog(this, "DADOS CADASTRADOS COM SUCESSO");
            this.limpar();
        } catch (Exception e) {
            //Mostra o erro na tela do usu√°rio
            JOptionPane.showMessageDialog(this, "ERRO inesperado: " + e.getMessage());
            e.printStackTrace(); //Mostra a stack no output
            jTextFieldNome.requestFocus();
        }
        
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    public void limpar(){
        jTextFieldNome.setText("");
        jTextFieldUnidadeMedida.setText("");
        jTextFieldNome.requestFocus();
    }
