    private void jTextFieldPesquisarPorNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisarPorNomeKeyReleased
        String campoPesquisa = (jTextFieldPesquisarPorNome.getText().trim());
        
        
        if(!campoPesquisa.equals("")){
            
            
            //table's model
            DefaultTableModel modelo = (DefaultTableModel) jTableProdutoCategoria.getModel();
            
            //Limpa as linhas da tabela
            modelo.setNumRows(0);
            
            try {
                ProdutoCategoriaDao dao = new ProdutoCategoriaDao();
                List<ProdutoCategoria> categorias = dao.buscarUsuario(campoPesquisa);
                
                for(ProdutoCategoria u : categorias){
                    //Cria uma nova linha
                    String novaLinha[] = {
                        String.valueOf(u.getId()),
                        u.getNome(),
                        ""
                    };
                    
                    modelo.addRow(novaLinha); //Adiciona linha na tabela
                }
                
                //DataBase search
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Erro ao pesquisar. " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jTextFieldPesquisarPorNomeKeyReleased

    private void jButtonMostrarTodosMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonMostrarTodosMouseReleased
        String campoPesquisa = (jTextFieldPesquisarPorNome.getText().trim());
        
        
        if(!campoPesquisa.equals("")){
            
            
            //table's model
            DefaultTableModel modelo = (DefaultTableModel) jTableProdutoCategoria.getModel();
            
            //Limpa as linhas da tabela
            modelo.setNumRows(0);
            
            try {
                ProdutoCategoriaDao dao = new ProdutoCategoriaDao();
                List<ProdutoCategoria> categorias = dao.buscarTodosNomes(campoPesquisa);
                
                for(ProdutoCategoria pc : categorias){
                    //Cria uma nova linha
                    String novaLinha[] = {
                        String.valueOf(pc.getId()),
                        pc.getNome(),
                        ""
                    };
                    
                    modelo.addRow(novaLinha); //Adiciona linha na tabela
                    jTextFieldPesquisarPorNome.setText("");
                    jTextFieldPesquisarPorNome.requestFocus();
                }
                
                //DataBase search
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Erro ao pesquisar. " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonMostrarTodosMouseReleased

