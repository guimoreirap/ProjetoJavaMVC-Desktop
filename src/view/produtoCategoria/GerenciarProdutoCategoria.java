    private void alterar(){
        int linhaSelecionda = jTableProdutoCategoria.getSelectedRow();
        
        if(linhaSelecionda != -1){
            
            //getModel pega a célula
            //getValueAt passa a linha e a coluna, a coluna 0 é o id, assim como a coluna 1 seria o nome
            String id = jTableProdutoCategoria.getModel().
                    getValueAt(linhaSelecionda, 0).toString();
            
            ProdutoCategoriaAlterar ua = new ProdutoCategoriaAlterar();
            ua.setVisible(true);
            ua.mostrarCategoria(id);
        }
    }
    
