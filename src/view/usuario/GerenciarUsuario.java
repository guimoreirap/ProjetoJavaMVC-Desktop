    private void jTextFieldPesquisarPorNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisarPorNomeKeyReleased
        String campoPesquisa = (jTextFieldPesquisarPorNome.getText().trim());
        
        
        if(!campoPesquisa.equals("")){
            
            
            //table's model
            DefaultTableModel modelo = (DefaultTableModel) jTableUsuario.getModel();
            
            //Limpa as linhas da tabela
            modelo.setNumRows(0);
            
            try {
                UsuarioDao dao = new UsuarioDao();
                List<Usuario> usuarios = dao.buscarUsuario(campoPesquisa);
                
                for(Usuario u : usuarios){
                    //Cria uma nova linha
                    String novaLinha[] = {
                        String.valueOf(u.getId()),
                        u.getNome(),
                        u.getEmail(),
                        ""
                    };
                    
                    modelo.addRow(novaLinha); //Adiciona linha na tabela
                }
                
                //DataBase search
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Erro ao pesquisar. " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jTextFieldPesquisarPorNomeKeyReleased

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int linhaSelecionada = jTableUsuario.getSelectedRow();
        
        int opcao = JOptionPane.showConfirmDialog(this, "Confirme a exclus√£o.");
        
        if(opcao == JOptionPane.YES_OPTION){
            if(linhaSelecionada != -1){
                /*
                    Pega os dados das colunas da tabela
                    Primeira coluna (0 que √© o id) √© inteiro
                */
                Integer id = Integer.parseInt(jTableUsuario.getModel().
                        getValueAt(linhaSelecionada, 0).toString());
                System.out.println(id);


                try {
                    Usuario u = new Usuario(id, null, null);
                    UsuarioDao dao = new UsuarioDao();
                    dao.excluir(u);

                    //Remove a linha da tabela
                    DefaultTableModel modelo = (DefaultTableModel) jTableUsuario.getModel();
                    modelo.removeRow(linhaSelecionada);

                    JOptionPane.showMessageDialog(this, "Registro excluido com sucesso.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir o registro" + ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Selecione o registro.");
            }
        }
        
    }//GEN-LAST:event_jButtonExcluirActionPerformed

