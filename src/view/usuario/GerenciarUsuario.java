    private void alterar(){
        int linhaSelecionda = jTableUsuario.getSelectedRow();
        
        if(linhaSelecionda != -1){
            
            //getModel pega a c√©lula
            //getValueAt passa a linha e a coluna, a coluna 0 √© o id, assim como a coluna 1 seria o nome
            String id = jTableUsuario.getModel().
                    getValueAt(linhaSelecionda, 0).toString();
            
            UsuarioAlterar ua = new UsuarioAlterar();
            ua.setVisible(true);
            ua.mostrarUsuario(id);
        }
    }
    
